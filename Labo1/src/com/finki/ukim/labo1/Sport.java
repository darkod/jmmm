package com.finki.ukim.labo1;

public class Sport {

	String ime;
	int slika;

	public Sport(String ime, int slika) {
		super();
		this.ime = ime;
		this.slika = slika;
	}

	public String getIme() {
		return ime;
	}

	public void setIme(String ime) {
		this.ime = ime;
	}

	public int getSlika() {
		return slika;
	}

	public void setSlika(int slika) {
		this.slika = slika;
	}

}
